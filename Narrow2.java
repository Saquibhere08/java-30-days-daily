class Narrow2
{
	public static void main(String [] args)
	{	
		byte b=34;
		short s = (short)b;
		int a = (int)b;
		long l = (long)b;
		float f = (float)b;
		
		
		System.out.println(b);
		System.out.println(f);
		System.out.println(l);
		System.out.println(a);
		System.out.println(s);
		System.out.println(b);

	}
}