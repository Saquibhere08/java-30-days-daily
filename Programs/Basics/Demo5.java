package Basics;
//loops
//while
public class Demo5 {
    public static void main(String[] args) {
        int x=1;
        System.out.println("While Loop");
        while (x<=9){
            System.out.println(x);
            x++;
        }
        System.out.println("Done");
    


        System.out.println("For Loop");
        for(int i=1;i<=9;i++){
            System.out.println(i);
        }
        System.out.println("Done.");
    }
    
}
