class Narrow1
{
	public static void main(String [] args)
	{	
		double d = 40.75;
		byte b=(byte) d;
		short s = (short)d;
		int a = (int)d;
		long l = (long)d;
		float f = (float)d;
		
		
		System.out.println(d);
		System.out.println(f);
		System.out.println(l);
		System.out.println(a);
		System.out.println(s);
		System.out.println(b);

	}
}